class FundingListCarouselModel {
  String title;
  String subtitle;
  String percentage;
  String rateOfReturn;
  String duration;
  String imageString;
  String fundingPercentage;

  FundingListCarouselModel(String title, String subtitle, String percentage, String rateOfReturn, String duration, String imageString, String fundingPercentage) {
    this.title = title;
    this.subtitle = subtitle;
    this.percentage = percentage;
    this.rateOfReturn = rateOfReturn;
    this.duration = duration;
    this.imageString = imageString;
    this.fundingPercentage = fundingPercentage;
  }
}